#include "includes/libft.h"

char	*ft_strncat(char *s1, char *s2, size_t n)
{
	char	*begin;

	begin = s1;
	while (*s1)
		s1++;
	while (n-- && *s2)
		*s1++ = *s2++;
	*s1 = '\0';
	return (begin);
}

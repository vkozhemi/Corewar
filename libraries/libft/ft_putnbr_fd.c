#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n / 10)
		ft_putnbr_fd((n / 10), fd);
	else if (n < 0)
		ft_putchar_fd('-', fd);
	ft_putchar_fd((ft_abs(n % 10) + '0'), fd);
}
